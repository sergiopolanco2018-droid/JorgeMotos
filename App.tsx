const CartSidebar = ({ 
  isOpen, 
  onClose, 
  items, 
  updateQuantity,
  removeItem,
  onCheckout
}: { 
  isOpen: boolean, 
  onClose: () => void, 
  items: CartItem[],
  updateQuantity: (id: string, delta: number) => void,
  removeItem: (id: string) => void,
  onCheckout: () => void
}) => {
  const navigate = useNavigate();
  const total = items.reduce((acc, item) => acc + (item.price * item.quantity), 0);

  return (
    <div className={`fixed inset-0 z-50 transform ${isOpen ? 'translate-x-0' : 'translate-x-full'} transition-transform duration-300 ease-in-out`}>
      <div className="absolute inset-0 bg-black/50" onClick={onClose} />
      <div className="absolute right-0 top-0 h-full w-full max-w-md bg-white shadow-xl flex flex-col">
        <div className="p-4 border-b border-slate-100 flex justify-between items-center">
          <h2 className="text-lg font-bold">Carrito de Compras</h2>
          <button onClick={onClose}><X /></button>
        </div>
        
        <div className="flex-1 overflow-y-auto p-4 space-y-4">
          {items.length === 0 ? (
            <div className="text-center py-20 text-slate-500 flex flex-col items-center">
              <ShoppingCart size={48} className="mx-auto mb-4 opacity-20" />
              <p className="mb-4">Tu carrito está vacío.</p>
              <Button variant="outline" size="sm" onClick={() => { onClose(); navigate('/shop'); }}>
                Ir a la Tienda
              </Button>
            </div>
          ) : (
            items.map(item => (
              <div key={item.id} className="flex gap-4 border-b border-slate-50 pb-4">
                <img src={item.image} alt={item.name} className="w-20 h-20 object-cover rounded-md" />
                <div className="flex-1">
                  <h4 className="font-semibold text-sm mb-1">{item.name}</h4>
                  <p className="text-slate-500 text-sm mb-2">${item.price.toLocaleString()}</p>
                  <div className="flex items-center gap-3">
                    <button 
                      onClick={() => updateQuantity(item.id, -1)}
                      className="w-6 h-6 rounded-full bg-slate-100 flex items-center justify-center hover:bg-slate-200"
                    >-</button>
                    <span className="text-sm w-4 text-center">{item.quantity}</span>
                    <button 
                      onClick={() => updateQuantity(item.id, 1)}
                      className="w-6 h-6 rounded-full bg-slate-100 flex items-center justify-center hover:bg-slate-200"
                    >+</button>
                    <button 
                      onClick={() => removeItem(item.id)}
                      className="ml-auto text-red-500 text-xs hover:underline"
                    >Eliminar</button>
                  </div>
                </div>
              </div>
            ))
          )}
        </div>

        {items.length > 0 && (
           <div className="p-4 border-t border-slate-100 bg-slate-50">
             <div className="flex justify-between mb-4 font-bold text-lg">
               <span>Total:</span>
               <span>${total.toLocaleString()}</span>
             </div>
             <Button className="w-full" onClick={onCheckout}>Proceder al Pago 🔒</Button>
           </div>
        )}
      </div>
    </div>
  );
};


