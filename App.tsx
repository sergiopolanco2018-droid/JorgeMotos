const ShopPage = ({ addToCart, products }: { addToCart: (p: Product) => void, products: Product[] }) => {
  const [searchTerm, setSearchTerm] = useState('');
  const [selectedCategory, setSelectedCategory] = useState<string>('all');
  const [priceRange, setPriceRange] = useState<[number, number]>([0, 5000000]);
  const location = useLocation();

  React.useEffect(() => {
    const params = new URLSearchParams(location.search);
    const cat = params.get('category');
    if (cat) setSelectedCategory(cat);
  }, [location]);

  const filteredProducts = products.filter(product => {
    const matchesSearch = product.name.toLowerCase().includes(searchTerm.toLowerCase());
    
    let matchesCategory = false;
    if (selectedCategory === 'all') {
      matchesCategory = true;
    } else if (selectedCategory === 'Bicicletas') {
      matchesCategory = [Category.BIKES_MOUNTAIN, Category.BIKES_ROAD, Category.BIKES_URBAN].includes(product.category);
    } else if (selectedCategory === 'Repuestos') {
       matchesCategory = [Category.PARTS_BRAKES, Category.PARTS_DRIVETRAIN, Category.PARTS_WHEELS].includes(product.category);
    } else {
      matchesCategory = product.category === selectedCategory;
    }

    const matchesPrice = product.price >= priceRange[0] && product.price <= priceRange[1];
    return matchesSearch && matchesCategory && matchesPrice;
  });

  const [viewProduct, setViewProduct] = useState<Product | null>(null);

  if (viewProduct) {
    return (
      <div className="container mx-auto px-4 py-12">
        <button onClick={() => setViewProduct(null)} className="mb-6 text-slate-500 hover:text-orange-600 flex items-center">
          <ArrowRight className="transform rotate-180 mr-2" /> Volver a la tienda
        </button>
        <div className="bg-white rounded-xl shadow-lg overflow-hidden border border-slate-100">
          <div className="grid grid-cols-1 md:grid-cols-2">
            <div className="h-96 md:h-full bg-slate-100 relative">
              <img src={viewProduct.image} alt={viewProduct.name} className="w-full h-full object-cover" />
            </div>
            <div className="p-8 md:p-12">
              <div className="text-orange-600 font-bold mb-2">{viewProduct.category}</div>
              <h1 className="text-3xl font-extrabold text-slate-900 mb-4">{viewProduct.name}</h1>
              <div className="flex items-center mb-6">
                <div className="flex text-yellow-400 mr-2">
                  {[...Array(5)].map((_, i) => (
                    <Star key={i} size={20} className={i < Math.floor(viewProduct.rating) ? "fill-current" : "text-gray-300"} />
                  ))}
                </div>
                <span className="text-slate-500 text-sm">{viewProduct.reviews} reseñas verificadas</span>
              </div>
              <p className="text-slate-600 text-lg mb-8 leading-relaxed">
                {viewProduct.description}
              </p>
              
              <div className="mb-8">
                <h3 className="font-bold text-slate-900 mb-3">Especificaciones Técnicas</h3>
                <div className="grid grid-cols-2 gap-4 text-sm">
                  {Object.entries(viewProduct.specs).map(([key, val]) => (
                    <div key={key} className="flex flex-col">
                      <span className="text-slate-400">{key}</span>
                      <span className="font-medium text-slate-700">{val}</span>
                    </div>
                  ))}
                </div>
              </div>

              {viewProduct.category.startsWith('Bicicletas') && ( 
                 <div className="mb-8 p-4 bg-slate-50 rounded-lg text-sm text-slate-600">
                    <span className="font-bold block mb-1">📏 Guía de Tallas Recomendada:</span>
                    <ul className="list-disc list-inside">
                        <li>1.60m - 1.70m: Talla S</li>
                        <li>1.70m - 1.80m: Talla M</li>
                        <li>1.80m+: Talla L</li>
                    </ul>
                 </div>
              )}

              <div className="flex items-center justify-between pt-6 border-t border-slate-100">
                <span className="text-3xl font-bold text-slate-900">${viewProduct.price.toLocaleString()}</span>
                <Button size="lg" onClick={() => addToCart(viewProduct)}>Agregar al Carrito</Button>
              </div>
            </div>
          </div>
        </div>
      </div>
    )
  }

  return (
    <div className="container mx-auto px-4 py-12">
      <div className="flex flex-col md:flex-row gap-8">
        {/* Sidebar Filters */}
        <aside className="w-full md:w-64 flex-shrink-0">
          <div className="bg-white p-6 rounded-lg shadow-sm border border-slate-200 sticky top-24">
            <h3 className="font-bold text-lg mb-4">Filtrar por</h3>
            
            <div className="mb-6">
              <label className="text-sm font-semibold text-slate-500 block mb-2">Categoría</label>
              <select 
                value={selectedCategory} 
                onChange={(e) => setSelectedCategory(e.target.value)}
                className="w-full border-slate-300 rounded-md shadow-sm focus:border-orange-500 focus:ring-orange-500"
              >
                <option value="all">Todas las Categorías</option>
                <option value="Bicicletas">🚲 Bicicletas (Todas)</option>
                <option value="Repuestos">⚙️ Repuestos (Todos)</option>
                <option disabled>──────────</option>
                {Object.values(Category).map(cat => (
                  <option key={cat} value={cat}>{cat}</option>
                ))}
              </select>
            </div>

            <div className="mb-6">
               <label className="text-sm font-semibold text-slate-500 block mb-2">Precio Máximo</label>
               <input 
                 type="range" 
                 min="0" 
                 max="5000000" 
                 step="100000"
                 value={priceRange[1]} 
                 onChange={(e) => setPriceRange([0, parseInt(e.target.value)])}
                 className="w-full h-2 bg-slate-200 rounded-lg appearance-none cursor-pointer accent-orange-600"
               />
               <div className="text-right text-sm text-slate-700 mt-2">${priceRange[1].toLocaleString()}</div>
            </div>
          </div>
        </aside>

        {/* Product Grid */}
        <div className="flex-1">
          <div className="mb-6 flex items-center bg-white p-4 rounded-lg border border-slate-200 shadow-sm">
            <Search className="text-slate-400 mr-3" />
            <input 
              type="text" 
              placeholder="Buscar bicicletas, repuestos..." 
              className="flex-1 outline-none text-slate-700"
              value={searchTerm}
              onChange={(e) => setSearchTerm(e.target.value)}
            />
          </div>

          {filteredProducts.length === 0 ? (
             <div className="text-center py-20">
               <p className="text-slate-500 text-lg">No encontramos productos con esos criterios.</p>
             </div>
          ) : (
            <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-6">
              {filteredProducts.map(product => (
                <ProductCard 
                  key={product.id} 
                  product={product} 
                  onAddToCart={addToCart} 
                  onClick={() => setViewProduct(product)}
                />
              ))}
            </div>
          )}
        </div>
      </div>
    </div>
  );
};

